func TestToInt(t *testing.T) {
	p := rune('p')
	L := rune('L')

	pInt := toInt(p)
	if pInt != 16 {
		t.Errorf("toInt(p) = %v, want %v", pInt, 16)
	}
	LInt := toInt(L)
	if LInt != 38 {
		t.Errorf("toInt(L) = %v, want %v", LInt, 38)
	}
}

